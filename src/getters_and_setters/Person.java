package getters_and_setters;

public class Person {
private String name;
private String superpower;
Person(){
	
}

String getname() {
	return this.name;
}
String getsuperpower() {
	return this.superpower;
}
void setname(String name) {
	this.name = name;
}
void setsuperpower(String superpower) {
	this.superpower = superpower;
}
}
